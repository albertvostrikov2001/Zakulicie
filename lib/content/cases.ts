import type { CaseStudy } from "@/lib/types";
import blurData from "@/lib/content/blur-data.json";
import { getCaseAssetMeta } from "@/lib/content/caseAssets";

type BlurEntry = {
  cover: { src: string; blurDataURL: string; width: number; height: number };
  gallery: { src: string; blurDataURL: string; width: number; height: number }[];
};

const bd = blurData as Record<string, BlurEntry>;

function blurKey(slug: string) {
  if (bd[slug]) return slug;
  const legacy = getCaseAssetMeta(slug)?.legacySlug;
  return legacy && bd[legacy] ? legacy : slug;
}

function cover(slug: string, alt: string, objectPosition?: string) {
  const focal = objectPosition ?? getCaseAssetMeta(slug)?.coverFocal;
  const key = blurKey(slug);
  const d = bd[key]?.cover;
  if (!d) return { src: `/cases/${slug}/cover.webp`, alt, objectPosition: focal };
  return {
    src: d.src.replace(`/cases/${key}/`, `/cases/${slug}/`),
    alt,
    blurDataURL: d.blurDataURL,
    width: d.width,
    height: d.height,
    objectPosition: focal,
  };
}

function gallery(slug: string, altFn: (i: number, total: number) => string) {
  const key = blurKey(slug);
  const items = bd[key]?.gallery ?? [];
  return items.map((g, i) => ({
    src: g.src.replace(`/cases/${key}/`, `/cases/${slug}/`),
    alt: altFn(i + 1, items.length),
    blurDataURL: g.blurDataURL,
    width: g.width,
    height: g.height,
  }));
}

